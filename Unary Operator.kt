fun main(args: Array<String>){
    var a=10
    var b=5
    var flag=true

    println("+a: " + +a )  //it will show value with sign i.e. 10
    println("-b: " + -b )   //it will show value with sign i.e. -5
    println("++a: " + ++a ) //11
    println("--b: " + --b ) //4
    println("!flag: " + !flag ) //false
}
